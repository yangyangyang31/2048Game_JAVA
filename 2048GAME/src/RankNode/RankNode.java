package RankNode;

public class RankNode {
    public int s;
    public String n;
    public RankNode(){}
    public RankNode(int s, String n){
        this.s = s;
        this.n = n;
    }
   public int getS() {
        return s;
   }
   public String getN()
   {
       return n;
   }

}
